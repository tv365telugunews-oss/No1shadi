// Get display name (capitalize first letter of each word)
export const getCasteDisplayName = (caste: string): string => {
  return caste
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
};

// Get value for form (lowercase with dashes)
export const getCasteValue = (caste: string): string => {
  return caste.toLowerCase().replace(/[\s\/\(\)]/g, '-');
};